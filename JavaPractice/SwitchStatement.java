package JavaPractice;

public class SwitchStatement{
    public static void main(String[] args) {
       
        char grade = 'A';

        switch(grade){
            case 'A':
                System.out.println("Outstanding");
                break;
            
            case 'B':
                System.out.println("Excellent");
                break;
            
            case 'C':
                System.out.println("Satisfactory");
                break;

        }

    }
}
